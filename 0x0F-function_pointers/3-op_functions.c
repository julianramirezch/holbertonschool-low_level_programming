#include "3-calc.h"

/**
 * op_add - Sum of a and b.
 * @a: Integer
 * @b: Integer
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Difference of a and b.
 * @a: Integer
 * @b: Integer
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Product of a and b.
 * @a: Integer
 * @b: Integer
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of a and b.
 * @a: Integer
 * @b: Integer
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Remainde of the division of a and b.
 * @a: Integer
 * @b: Integer
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
